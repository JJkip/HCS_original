        <h2>Programme Impact, Objectives & Outcomes</h2>
 <p>
            The planned impact of the HCS is improved survival and health status of the Somali people. The planned outcome of the HCS is that â€œSomali people, especially women, children and the most vulnerable in target areas increase utilisation of quality health services contributing to the Millennium Development Goals 1,4,5,6 (this includes reducing poverty and hunger, improving reproductive, maternal and child health, and the prevention and treatment of AIDS, TB and malaria.
        </p>
        <p>The HCS is focused on helping the three regional health authorities of Somaliland, Puntland and the Transitional Federal Government (SFG) put into action the EPHS framework which they adopted in 2009. This sets out 10 priority health interventions to be implemented at primary health units (community), health centres, and local and regional hospitals, with overarching support for nutrition. The framework includes activities that increase demand, such as community meetings and radio and mobile phone messages, increasing awareness of the benefits of child and birth spacing and safe delivery, improving health clinics, providing family planning, ante-natal care and safe delivery, caring for new-borns, giving vaccinations, preventing and treating pneumonia, diarrhoea, malaria, TB and AIDS, as well as screening for and treating malnutrition. The HCS is addressing all EPHS issues, but with a priority on RMNH and family planning and child health.</p>
