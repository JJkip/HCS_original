        <h2>Health Poverty Action (HPA)</h2>
        <p>Health Poverty Action (HPA) formerly known as Health Unlimited is a British international, development organization with a mission to support the poorest and most vulnerable people to achieve better health and wellbeing. HPA works in 14 countries spread across Africa, South East Asia, East Asia and Latin America. Health Poverty Action has extensive experience in the Horn of Africa having implemented health system strengthening and behavior change communication programmes in Somalia particularly in Somaliland, Ethiopia and Kenya.</p>
        <p>In Somaliland, HPA operates in two main regions; Maroodi Jeex and Sahil. We target IDPs and people living in hard to reach areas often unable to access quality health care. HPA employs a comprehensive and integrated approach on maternal, child health, health education, nutrition, and sexual and gender based violence issues. Our programs target women of reproductive age, pregnant women, new born and adolescents. We work in partnership with the Government of Somaliland, local and international organizations and the communities to improve health status of the people.</p>
        <h2>PSI/SOMALILAND</h2>
        <p>
            PSI/Somaliland is the lead agency of the consortium. Working in partnership with the private and public sector and with other development partners, PSI/Somaliland socially markets life-saving maternal and child health products and provides behavioral change communication interventions throughout Somaliland.
        </p>
        <h2>Save the Children, UK (SC-UK)</h2>
        <p>Save the Children International is the world's leading independent organization for children. SCI works in around 120 countries. We save children's lives; we fight for their rights; we help them fulfill their potential. SCI in Somalia /Somaliland works in partnership with public sectors and civil societies in various service delivery areas including in the health, education, nutrition, food security/livelihood, and child protection sectors.</p>
        <p>SCI works to inspire breakthroughs in the way the world treats children and to achieve immediate and lasting change in their lives. Across all of our work, we pursue several core values: accountability, ambition, collaboration, creativity and integrity.</p>
        <h2>Trocaire (Ireland)</h2>
        <p>Trócaire is an Irish development agency that has been working in five districts in Northern Gedo region (Luuq, Bula Hawa, Garbahareey, Dollow and Burdhubo) south-west Somalia, since 1992. Through health, nutrition and education programs, Trócaire works closely with local communities to provide essential medical and education services to approximately 220,000 people,ethrough 3 district hospitals, 5 Maternal Child Health Centres, 3 Tuberculosis centres, 50 health posts and 5 outreach teams. We work to create conditions for these communities to be more resilient, hence ensuring long-term and sustainable access to basic services for the population.</p>
        <h2>Tropical Health Education Trust (THET)</h2>
        <p>
            THET Somaliland takes an integrated approach to Human Resources for Health and works at three levels; Individual health workers; Civil society institutions; and Government.Through our partnerships we seek to achieve; Better quality teaching and training for health workers in Somaliland with a special focus on doctors, qualified nurses and midwives; Strong partner institutions with improved skills and resources, better able to carry out their mandate within the Somaliland health sector; Strengthened governance structures, managing and supporting the quality delivery of health services in Somaliland.
        </p>
